function find_similar(percent_m) {
	var diff = Infinity;
	var job = 0;
	for (var i =  0; i < male.length; i++) {
		if (abs(percent_m - female[i]/(male[i]+female[i])) < diff){
			job = i;
			diff = abs(percent_m - female[i]/(male[i]+female[i]));
		}
	}
	return (job);
}
