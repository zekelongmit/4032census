function keyTyped() {
  if (key === 'a') {
    middle_x += 50;
  } 
  else if (key === 'w') {
    middle_y += 50;
  }
  else if (key === 's') {
    middle_y -= 50;
  }
  else if (key === 'd') {
    middle_x -= 50;
  }
  else if (key === '=') {
    scale_factor = scale_factor*1.5;
  }
  else if (key === '-') {
    scale_factor = scale_factor/1.5;
  }
  else{
  }
}

function mousePressed(){
	if(stop_looping == false){
		for (var i = 0; i < circles.length; i++) {
			if (circles[i].active){
				final = i;
				stop_looping = true;
				break;
			}
  		}
  	}
  	else{
  		var dist_to_back = dist(mouseX, mouseY, 600,600-170);
  		var dist_to_rand = dist(mouseX, mouseY, 600,170);

  		if (dist_to_back < 60){
  			stop_looping = false;
  			clear();
  			setup();
		}
		if (dist_to_rand < 60){
			new_job = floor(random(0,500));
			final = new_job;
		}
  	}
}

